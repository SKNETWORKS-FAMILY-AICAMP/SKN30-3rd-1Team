const dictionaries: Record<LanguageSetting, Record<string, string>> = { en, ko };
const LanguageContext = createContext<LanguageSetting>("ko");

export function translate(language: LanguageSetting, key: string, vars: Vars = {}) {
  const template = dictionaries[language][key] ?? key;
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(vars[name] ?? ""));
}

export function I18nProvider({ children, language }: { children: ReactNode; language: LanguageSetting }) {
  return createElement(LanguageContext.Provider, { value: language }, children);
}

export function useI18n() {
  const language = useContext(LanguageContext);
  return {
    language,
    t: (key: string, vars?: Vars) => translate(language, key, vars),
  };
}
